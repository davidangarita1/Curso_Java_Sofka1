package com.sofka;

public class CiclosSeries {
    public static void main(String[] args) {
        // Algoritmos ciclicos
        // Serie de numeros impares
        for(int i=0;i<=100;i++)
        {
            if((i%2) != 0)
            {
                System.out.println(i);
            }
        }
    }
}
